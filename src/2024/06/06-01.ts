let matrix = s.split('\n').map(line => line.split(''));

type Direction = 'up' | 'right' | 'down' | 'left';

let currentDirection: Direction = 'up';

const allXCordinates: { x: number, y: number, direction: Direction }[] = [];

lo : while (true) {
  for (let y = 0; y < matrix.length; y++) {
    const row = matrix[y];
    for (let x = 0; x < row.length; x++) {
      let cha = row[x];
      if (cha === '^') {

        const cordinates = getCordinates(x, y, currentDirection);
        const nextEl = (matrix[cordinates.y] ?? [])[cordinates.x];
        if (nextEl === undefined) {
          matrix[y][x] = 'X';
          allXCordinates.push({x, y, direction: currentDirection});
          break lo;
        } else if (nextEl === '#') {
          currentDirection = getNextDirection(currentDirection);
          continue lo;
        } else {
          matrix[cordinates.y][cordinates.x] = '^';
          matrix[y][x] = 'X';
          allXCordinates.push({x, y, direction: currentDirection});
        }
        continue lo;
      }
    }
  }
}

function getNextDirection(d: Direction): Direction {
  const strings: Direction[] = ['up', 'right', 'down', 'left'];
  return strings[strings.indexOf(d) + 1] ?? strings[0];
}

// todo utils
function getCordinates(x, y, d: Direction): { x: number, y: number } {
  if (d === 'up') {
    return {x: x + 0, y: y - 1};
  } else if (d === 'right') {
    return {x: x + 1, y: y + 0};
  } else if (d === 'down') {
    return {x: x + 0, y: y + 1};
  } else if (d === 'left') {
    return {x: x - 1, y: y + 0};
  }
  return undefined;
}

